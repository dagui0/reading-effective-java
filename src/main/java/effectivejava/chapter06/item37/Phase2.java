package effectivejava.chapter06.item37;

/**
 * 물질의 상태를 나타내는 열거형입니다.
 * 각 상은 물질의 물리적 상태를 설명합니다.
 */
public enum Phase2 {

    /**
     * 고체:
     * 원자/분자가 고정된 위치에서 진동. 형태와 부피 일정.
     */
    SOLID,

    /**
     * 액체:
     * 원자/분자가 서로 가깝지만 비교적 자유롭게 이동. 부피 일정, 형태는 용기 따름.
     */
    LIQUID,

    /**
     * 기체:
     * 원자/분자가 멀리 떨어져 매우 자유롭게 운동. 형태와 부피 일정하지 않음.
     */
    GAS,

    /**
     * 플라스마:
     * 기체가 초고온 상태에서 이온(원자핵)과 전자로 분리된 상태.
     * 우주에서 가장 흔한 상태 (별, 번개 등). 종종 '제4의 물질 상태'라고 불림.
     */
    PLASMA,

    /**
     * 보스-아인슈타인 응축 (BEC):
     * 극저온에서 보손(boson) 입자들이 양자 역학적으로 동일한 가장 낮은 에너지 상태로
     * '겹쳐진(응축된)' 상태. 매우 특이한 양자 상태.
     */
    BOSE_EINSTEIN_CONDENSATE,

    /**
     * 페르미온 응축:
     * 극저온에서 페르미온(fermion) 입자들이 쌍을 이루어(쿠퍼 쌍처럼)
     * 보스-아인슈타인 응축과 유사한 거시적인 양자 상태를 형성한 것. 초전도 현상과 관련.
     */
    FERMIONIC_CONDENSATE,

    /**
     * 쿼크-글루온 플라스마 (QGP):
     * 극도로 높은 온도와 압력에서 양성자나 중성자를 이루는 기본 입자인
     * 쿼크(quark)와 글루온(gluon)이 핵 속박에서 벗어나 자유롭게 돌아다니는 상태.
     * 빅뱅 직후 초기 우주 상태로 추정.
     */
    QUARK_GLUON_PLASMA,

    /**
     * 초유체:
     * 극저온에서 점성(끈끈함)이 완전히 사라져 저항 없이 흐를 수 있는 액체 상태. 양자 역학적 현상.
     */
    SUPERFLUID,

    /**
     * 초전도체:
     * 특정 온도(임계 온도) 이하에서 전기 저항이 완전히 0이 되는 물질의 상태. 에너지 손실 없이 전류를 흘릴 수 있음.
     * (엄밀히는 물질의 전기적 상태지만, 상전이 현상으로 간주하여 물질 상태에 포함시키기도 함)
     */
    SUPERCONDUCTOR,

    /**
     * 액정:
     * 액체처럼 흐르는 성질(유동성)과 고체 결정처럼 분자들이
     * 특정 방향으로 배열되는 성질(방향성)을 동시에 가지는 물질 상태.
     * LCD 디스플레이 등에 사용됨. 고체와 액체의 중간 상태(mesophase).
     */
    LIQUID_CRYSTAL,

    /**
     * 에어로겔:
     * 젤(gel) 구조에서 액체 성분 대신 기체(주로 공기)를 채워 만든,
     * 매우 가볍고 다공성이며 단열성이 뛰어난 고체 물질.
     * (엄밀히는 '물질의 상태'라기보다는 특별한 구조의 '소재'에 가까움)
     */
    AEROGEL,

    /**
     * 준금속:
     * 금속과 비금속의 중간적인 성질을 가지는 화학 원소들의 분류 (예: 규소(Si), 저마늄(Ge)).
     * (물질의 '상태'가 아니라 '원소의 분류'임)
     */
    METALLOID,

    /**
     * 이온화된 기체:
     * 기체 상태에서 원자나 분자가 전자를 잃거나 얻어 이온(ion) 상태로 존재하는 것.
     * **사실상 플라스마(PLASMA)와 거의 같은 상태를 의미합니다.** (목록에 중복이 있는 것으로 보임)
     */
    IONIZED_GAS,

    /**
     * 축퇴 물질:
     * 극도로 높은 밀도와 압력 하에서 물질이 양자 역학적인 효과(축퇴압, degeneracy pressure)에 의해 지탱되는 상태.
     * 일반적인 기체 법칙을 따르지 않음. 백색왜성이나 중성자별 내부의 물질 상태.
     */
    DEGENERATE_MATTER;
}
